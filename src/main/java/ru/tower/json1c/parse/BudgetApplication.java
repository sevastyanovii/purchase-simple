package ru.tower.json1c.parse;

import java.util.Date;

public class BudgetApplication {

    private Date date_budget_application;
    private String number_budget_application;

    public Date getDate_budget_application() {
        return date_budget_application;
    }

    public void setDate_budget_application(Date date_budget_application) {
        this.date_budget_application = date_budget_application;
    }

    public String getNumber_budget_application() {
        return number_budget_application;
    }

    public void setNumber_budget_application(String number_budget_application) {
        this.number_budget_application = number_budget_application;
    }
}
